def greet(name):
    """
    Greets the given name.
    """
    print(f"Hello, {name}!")

def add_numbers(a, b):
    """
    Adds two numbers.
    """
    return a + b

def multiply_numbers(a, b):
    """
    Multiplies two numbers.
    """
    return a * b

class Calculator:
    def __init__(self):
        self.result = 0

    def subtract(self, a, b):
        return a - b
